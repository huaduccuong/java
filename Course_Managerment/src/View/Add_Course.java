    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Course_DAO dao3 = new Course_DAO(this.vector);
        String code = txtCode.getText();
        String name = txtName.getText();
        String credit = txtCredit.getText();
        if (check == true && !code.equals("") && !name.equals("") && !credit.equals("")) {

            Course course = new Course(code,dao3.checkFormatString(txtName.getText()), Integer.parseInt(credit));
            boolean bool = dao3.addCourse(course);
            if (bool) {
                JOptionPane.showMessageDialog(parent, "Add successfully");
                txtCode.setText("");
                txtCredit.setText("");
                txtName.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Add field ");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

