    private void txtCreditFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCreditFocusLost
        if (count == 2&&checkClear==false) {
            String credit = txtCredit.getText();
            Course_DAO dao1 = new Course_DAO(vector);
            if (dao1.checkInputValues(credit, 3)) {
                int credits = Integer.parseInt(txtCredit.getText());
                if (credits >= 0 && credits <= 33) {
                    check = true;
                } else {
                    check = false;
                    JOptionPane.showMessageDialog(this, "Out of rangle ");
                    txtCredit.requestFocus();
                }
            } else {
                check = false;
                JOptionPane.showMessageDialog(this, "Invalid value");
                txtCredit.requestFocus();

            }
        }

    }//GEN-LAST:event_txtCreditFocusLost

